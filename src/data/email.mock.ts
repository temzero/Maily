const getCurrentUserFormatted = () => {
  const user = currentUser;
  return `${user.displayName || user.firstName} <${user.email}>`;
};

const now = new Date();
const getTimeAgo = (days: number, hours: number = 0) => {
  const date = new Date(now);
  date.setDate(date.getDate() - days);
  date.setHours(date.getHours() - hours);
  return date.toISOString();
};

// Helper function to get envelope by name
const getEnvelope = (name: string) => {
  return getEnvelopeByName(name) || envelopePresets[0]; // Fallback to first preset
};

